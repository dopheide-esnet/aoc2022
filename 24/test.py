
import pytest
from aoc24 import BuildMap, Blizzards

def test_read_map():
    lines = ['#E######', '#>>.<^<#', '#.<..<<#', '#>v.><>#', '#<^v^^>#', '######.#']
    map = BuildMap(lines)
    assert map == [[['#'], ['E'], ['#'], ['#'], ['#'], ['#'], ['#'], ['#']], [['#'], ['>'], ['>'], ['.'], ['<'], ['^'], ['<'], ['#']], [['#'], ['.'], ['<'], ['.'], ['.'], ['<'], ['<'], ['#']], [['#'], ['>'], ['v'], ['.'], ['>'], ['<'], ['>'], ['#']], [['#'], ['<'], ['^'], ['v'], ['^'], ['^'], ['>'], ['#']], [['#'], ['#'], ['#'], ['#'], ['#'], ['#'], ['.'], ['#']]]

